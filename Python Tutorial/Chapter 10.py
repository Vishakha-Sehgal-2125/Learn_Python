# If elif else condition statement  
""" 
If else and elif statements are a multiway decision 
taken by our programme in which some conditions are applied 
if any condition is true then it do not checks other conditions 
it directly jumps out of the code. 
"""
# Syntax
"""
if(condition):
   block of code
elif(condition):
   block of code
else:
   block of code
jumps out of program   
"""
a = int(input("Enter your age:"))

if(a>=18):
    print("Eligible")

elif(a==0):
    print("Get out of your mother's womb")

else: 
    print("Not eligible")

print("End of Program")    

